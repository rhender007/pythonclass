# Division operator /

print(10 / 3)

# Floor operator, or called as Truncation Division //

print(10 // 3)

# Modulo operator

print( 27 % 7)

# Shift operator - Right Shift - Binary Operations
# It's a right bit shift operator, 'moves' all bits once to the right

print(10 >> 1)
print(bin(10))
print(bin(1))
print(bin(5))

# Shift operator - Left Shift

print(100 << 20)



